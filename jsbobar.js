function moreinfo() {
   if (document.querySelector('#button1').classList.contains('popup-off')) {
      document.querySelector('#button1').classList.remove('popup-off');
      document.querySelector('#button1').classList.add('popup-on');
   } else {
      document.querySelector('#button1').classList.remove('popup-on');
      document.querySelector('#button1').classList.add('popup-off');
   }
}
